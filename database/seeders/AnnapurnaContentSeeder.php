    /**
     * Copy a public theme image to storage so Cms::imageUrl() can serve it.
     * Source: public/annapurna/img/{filename}
     * Dest:   storage/app/public/seeded/annapurna/{filename}
     */
    private function seedImage(string $publicPath): ?string
    {
        $sourcePath  = public_path($publicPath);
        $relative    = 'seeded/' . ltrim($publicPath, '/');
        $destPath    = storage_path('app/public/' . $relative);

        if (file_exists($sourcePath)) {
            $dir = dirname($destPath);
            if (!is_dir($dir)) {
                mkdir($dir, 0755, true);
            }
            copy($sourcePath, $destPath);
            return $relative;
        }

        return null;
    }
